package com.project.vo;

public class TestVO {

	private String orgCode;
	private String orgName;
	private int rCurrentPerson;
	private int rCurrentCar;

	public TestVO() {
	}

	public TestVO(String orgCode, String orgName, int rCurrentPerson, int rCurrentCar) {
		super();
		this.orgCode = orgCode;
		this.orgName = orgName;
		this.rCurrentPerson = rCurrentPerson;
		this.rCurrentCar = rCurrentCar;
	}

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public int getrCurrentPerson() {
		return rCurrentPerson;
	}

	public void setrCurrentPerson(int rCurrentPerson) {
		this.rCurrentPerson = rCurrentPerson;
	}

	public int getrCurrentCar() {
		return rCurrentCar;
	}

	public void setrCurrentCar(int rCurrentCar) {
		this.rCurrentCar = rCurrentCar;
	}

}
