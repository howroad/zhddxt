package com.project.vo;

public class ReportVO {
	private String rcode;
	private String userName;
	private String userTel;
	private String rname;
	private String rregion;
	private String raddress;
	private String rgrade;
	private String rtype;
	private String rdate;
	private String rtel;
	private String rinitialLoss;
	private String rnum;
	private String rarea;
	private String rimage;
	private String rdesc;

	public ReportVO() {
	}

	public ReportVO(String rcode, String userName, String userTel, String rname, String rregion, String raddress,
			String rgrade, String rtype, String rdate, String rtel, String rinitialLoss, String rnum, String rarea,
			String rimage, String rdesc) {
		this.rcode = rcode;
		this.userName = userName;
		this.userTel = userTel;
		this.rname = rname;
		this.rregion = rregion;
		this.raddress = raddress;
		this.rgrade = rgrade;
		this.rtype = rtype;
		this.rdate = rdate;
		this.rtel = rtel;
		this.rinitialLoss = rinitialLoss;
		this.rnum = rnum;
		this.rarea = rarea;
		this.rimage = rimage;
		this.rdesc = rdesc;
	}

	public String getRcode() {
		return rcode;
	}

	public void setRcode(String rcode) {
		this.rcode = rcode;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserTel() {
		return userTel;
	}

	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}

	public String getRname() {
		return rname;
	}

	public void setRname(String rname) {
		this.rname = rname;
	}

	public String getRregion() {
		return rregion;
	}

	public void setRregion(String rregion) {
		this.rregion = rregion;
	}

	public String getRaddress() {
		return raddress;
	}

	public void setRaddress(String raddress) {
		this.raddress = raddress;
	}

	public String getRgrade() {
		return rgrade;
	}

	public void setRgrade(String rgrade) {
		this.rgrade = rgrade;
	}

	public String getRtype() {
		return rtype;
	}

	public void setRtype(String rtype) {
		this.rtype = rtype;
	}

	public String getRdate() {
		return rdate;
	}

	public void setRdate(String rdate) {
		this.rdate = rdate;
	}

	public String getRtel() {
		return rtel;
	}

	public void setRtel(String rtel) {
		this.rtel = rtel;
	}

	public String getRinitialLoss() {
		return rinitialLoss;
	}

	public void setRinitialLoss(String rinitialLoss) {
		this.rinitialLoss = rinitialLoss;
	}

	public String getRnum() {
		return rnum;
	}

	public void setRnum(String rnum) {
		this.rnum = rnum;
	}

	public String getRarea() {
		return rarea;
	}

	public void setRarea(String rarea) {
		this.rarea = rarea;
	}

	public String getRimage() {
		return rimage;
	}

	public void setRimage(String rimage) {
		this.rimage = rimage;
	}

	public String getRdesc() {
		return rdesc;
	}

	public void setRdesc(String rdesc) {
		this.rdesc = rdesc;
	}

}
